CREATE SCHEMA IF NOT EXISTS NINTENDO;

-- Project Details
CREATE TABLE NINTENDO.PROJECT (
    PROJECT_ID varchar(36) PRIMARY KEY NOT NULL,
    TEAM_ID varchar(10),
    FRANCHISE_ID varchar(36) NOT NULL,
    PROJECT_NM varchar(50) NOT NULL, 
    PROJECT_STATUS varchar(10) NOT NULL,
    MODIFIED timestamp NOT NULL
);

CREATE UNIQUE INDEX XPK_PROJECT ON NINTENDO.PROJECT(PROJECT_ID);
CREATE INDEX PROJECT_IDX1 ON NINTENDO.PROJECT(TEAM_ID);
CREATE INDEX PROJECT_IDX2 ON NINTENDO.PROJECT(FRANCHISE_ID);

